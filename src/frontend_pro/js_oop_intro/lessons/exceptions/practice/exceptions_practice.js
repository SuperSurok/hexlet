import ParseError from './ParseError.js';

//////////////////////////////////////////////////////
///////////////// First Solution /////////////////////
//////////////////////////////////////////////////////
export const parseJson = (data) => {
  try {
    return JSON.parse(data);
  } catch (err) {
    throw new ParseError('Invalid JSON string');
  }
};

